# Variabel atau Variables adalah tempat untuk menyimpan data
x = 5
y = "Hello, World!"

# Mengakses Variabel
print(x)
print(y)

# Casting atau Mengubah Tipe Data
a = str(10)    # Mengubah integer ke string
b = int(10.5)  # Mengubah float ke integer
c = float(10)  # Mengubah integer ke float

# Mendapatkan Tipe Data
print(type(a))
print(type(b))
print(type(c))

