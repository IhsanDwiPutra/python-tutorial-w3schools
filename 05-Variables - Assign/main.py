# Banyak nilai atau value di multi variabel
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# Satu nilai atau value di multi variabel
x = y = z = "Orange"
print(x)
print(y)
print(z)

# Unpack a collection
fruits = ["Apple", "Banana", "Cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)